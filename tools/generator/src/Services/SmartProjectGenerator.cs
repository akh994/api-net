    private string EnsureHttpAnnotations(string protoContent, string entityName)
    {
        // 1. Ensure google/api/annotations.proto import
        if (!protoContent.Contains("google/api/annotations.proto"))
        {
            var syntaxMatch = Regex.Match(protoContent, @"syntax\s*=\s*""[^""]+"";");
            if (syntaxMatch.Success)
            {
                protoContent = protoContent.Insert(syntaxMatch.Index + syntaxMatch.Length, "\nimport \"google/api/annotations.proto\";");
            }
        }

        // 2. Add HTTP options to RPCs that don't have them
        var rpcRegex = new Regex(@"rpc\s+(\w+)\s*\(([^)]+)\)\s+returns\s*\(([^)]+)\)\s*([;{])", RegexOptions.Multiline);
        protoContent = rpcRegex.Replace(protoContent, m =>
        {
            var rpcName = m.Groups[1].Value;
            var requestType = m.Groups[2].Value;
            var responseType = m.Groups[3].Value;
            var terminator = m.Groups[4].Value;

            // If it already has options (starts with {), check for http
            if (terminator == "{")
            {
                // We need to find the matching closing brace to check content
                // Simplification for now: check if the rest of the file until next rpc or end contains google.api.http
                var remaining = protoContent.Substring(m.Index);
                var nextRpc = remaining.IndexOf("rpc ", 4);
                var block = nextRpc > 0 ? remaining.Substring(0, nextRpc) : remaining;

                if (block.Contains("google.api.http"))
                {
                    return m.Value;
                }
            }

            var method = "post";
            if (rpcName.StartsWith("Get", StringComparison.OrdinalIgnoreCase) ||
                rpcName.StartsWith("List", StringComparison.OrdinalIgnoreCase) ||
                rpcName.StartsWith("Search", StringComparison.OrdinalIgnoreCase))
            {
                method = "get";
            }
            else if (rpcName.StartsWith("Update", StringComparison.OrdinalIgnoreCase))
            {
                method = "put";
            }
            else if (rpcName.StartsWith("Delete", StringComparison.OrdinalIgnoreCase))
            {
                method = "delete";
            }

            var path = $"/v1/{entityName.ToLower()}";
            if (method == "get" && !rpcName.Equals($"Get{entityName}", StringComparison.OrdinalIgnoreCase) &&
                !rpcName.Equals($"List{entityName}s", StringComparison.OrdinalIgnoreCase))
            {
                path = $"/v1/{entityName.ToLower()}/{rpcName.ToLower()}";
            }
            else if (method == "delete" || (method == "get" && rpcName.Equals($"Get{entityName}", StringComparison.OrdinalIgnoreCase)))
            {
                path = $"/v1/{entityName.ToLower()}/{{id}}";
            }
            else if (method == "put")
            {
                path = $"/v1/{entityName.ToLower()}/{{id}}";
            }

            var option = $"\n        option (google.api.http) = {{\n            {method}: \"{path}\"";
            if (method == "post" || method == "put")
            {
                option += ",\n            body: \"*\"";
            }
            option += "\n        };";

            if (terminator == ";")
            {
                return $"rpc {rpcName} ({requestType}) returns ({responseType}) {{{option}\n    }}";
            }
            else // terminator == "{"
            {
                return $"rpc {rpcName} ({requestType}) returns ({responseType}) {{{option}";
            }
        });

        return protoContent;
    }


    private async Task UpdateProductDocAsync(string targetDir, string projectName, bool dryRun)
    {
        if (dryRun) return;

        var productDocPath = Path.Combine(targetDir, "docs", "steering", "product.md");
        if (!File.Exists(productDocPath)) return;

        var content = await File.ReadAllTextAsync(productDocPath);
        var lines = content.Split(new[] { "\r\n", "\r", "\n" }, StringSplitOptions.None);
        var updatedLines = new List<string>();

        var kebabCase = ToKebabCase(projectName);
        foreach (var line in lines)
        {
            var updatedLine = line;

            // Update Product Title
            if (line.StartsWith("# Product: "))
            {
                updatedLine = $"# Product: {projectName}";
            }
            // Update Purpose Section
            else if (line.Contains($"**{kebabCase}**"))
            {
                var note = " Project ini mengambil dari template projek skeleton-api-net yang dikembangkan oleh Tim Arsitek.";
                if (!line.Contains(note))
                {
                    updatedLine = line;
                    if (!updatedLine.TrimEnd().EndsWith("."))
                    {
                        updatedLine = updatedLine.TrimEnd() + ".";
                    }
                    updatedLine += note;
                }
            }

            updatedLines.Add(updatedLine);
        }

        await File.WriteAllTextAsync(productDocPath, string.Join("\n", updatedLines));
        AnsiConsole.MarkupLine("[green]Updated product.md in docs/steering[/]");
    }

