        if (!insertFields.Any(f => f == "updated_at"))
            insertFields.Add("updated_at");

        var insertFieldsStr = string.Join(", ", insertFields);
        var insertFieldsParams = string.Join(", ", insertFields.Select(f => $"@{ToPascalCase(f)}"));

