    private async Task GenerateMigrationsAsync(Models.EntityDefinition entity, string targetDir, bool dryRun, int offsetSeconds = 0)
    {
        var migrationsDir = Path.Combine(targetDir, "migrations");
        if (!dryRun) Directory.CreateDirectory(migrationsDir);

        var timestamp = DateTime.Now.AddSeconds(offsetSeconds).ToString("yyyyMMddHHmmss");
        var tableName = entity.TableName;
        var upFileName = $"{timestamp}_create_{tableName}_table.up.sql";
        var downFileName = $"{timestamp}_create_{tableName}_table.down.sql";

        var sb = new System.Text.StringBuilder();
        sb.AppendLine($"-- Create {tableName} table");
        sb.AppendLine($"CREATE TABLE IF NOT EXISTS {tableName} (");

        var columns = new List<string>();
        foreach (var field in entity.Fields)
        {
            var columnName = RenameIdentifier(field.Name, "").ToLower(); // Simple snake_case conversion approximation
            // Better snake_case:
            columnName = System.Text.RegularExpressions.Regex.Replace(field.Name, "([a-z])([A-Z])", "$1_$2").ToLower();

            var sqlType = GetSqlType(field.Type);
            var constraints = "";

            if (columnName == "id")
            {
                if (sqlType == "INT" || sqlType == "BIGINT")
                {
                    constraints = " PRIMARY KEY AUTO_INCREMENT";
                }
                else
                {
                    sqlType = "VARCHAR(36)";
                    constraints = " PRIMARY KEY";
                }
            }
            else if (columnName == "created_at")
            {
                sqlType = "TIMESTAMP";
                constraints = " DEFAULT CURRENT_TIMESTAMP";
            }
            else if (columnName == "updated_at")
            {
                sqlType = "TIMESTAMP";
                constraints = " DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP";
            }
            else if (!field.Nullable && field.Type != "string?") // string? is nullable
            {
                // For other non-nullable types, we might want NOT NULL, but let's be lenient for now unless it's critical
                // Actually, in proto everything is optional by default in proto3, but we mapped some to nullable.
                // If it is NOT nullable in C#, it should be NOT NULL in SQL.
                if (!field.Type.EndsWith("?"))
                {
                    constraints = " NOT NULL";
                }
            }

            columns.Add($"    {columnName} {sqlType}{constraints}");
        }

        // Ensure audit columns exist as they are used in Repositories
        if (!columns.Any(c => c.TrimStart().StartsWith("created_at ")))
        {
            columns.Add("    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP");
        }
        if (!columns.Any(c => c.TrimStart().StartsWith("updated_at ")))
        {
            columns.Add("    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP");
        }

        sb.AppendLine(string.Join(",\n", columns));
        sb.AppendLine(") ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;");

        await WriteFileAsync(Path.Combine(migrationsDir, upFileName), sb.ToString(), dryRun, null);

        var downContent = $"-- Drop {tableName} table\nDROP TABLE IF EXISTS {tableName};\n";
        await WriteFileAsync(Path.Combine(migrationsDir, downFileName), downContent, dryRun, null);
    }

    private string GetSqlType(string csharpType)
    {
        return csharpType switch
        {
            "string" or "string?" => "VARCHAR(255)",
            "int" or "int?" => "INT",
            "long" or "long?" => "BIGINT",
            "double" or "double?" => "DOUBLE",
            "float" or "float?" => "FLOAT",
            "bool" or "bool?" => "BOOLEAN",
            "DateTime" or "DateTime?" => "TIMESTAMP",
            _ => "VARCHAR(255)"
        };
    }

