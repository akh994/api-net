                };
                databaseSection["Port"] = port;

                var options = new JsonSerializerOptions { WriteIndented = true };
                await File.WriteAllTextAsync(filePath, jsonNode.ToJsonString(options));
            }
        }
        catch (Exception ex)
        {
            AnsiConsole.MarkupLine($"[red]Error updating appsettings.json: {ex.Message}[/]");
        }
    }

    private async Task GenerateCommonFilesAsync(List<Models.EntityDefinition> entities, string projectName, string targetDir, bool dryRun, bool includeMq)
    {
