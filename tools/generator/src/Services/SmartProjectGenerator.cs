    private string GenerateProgramCs(List<Models.EntityDefinition> entities, string projectName, bool includeMq)
    {
        // Use the first entity to get the base content
        var baseContent = GenerateProgramCs(entities.First(), projectName, includeMq);

        // Construct the list of mappings
        var mappings = new List<string>();
        foreach (var entity in entities)
        {
            mappings.Add($"app.MapGrpcService<{entity.ServiceName}Handler>();");
        }
        var mappingsStr = string.Join("\n", mappings);

        // Replace the single mapping with the list
        // Note: The base GenerateProgramCs uses {entity.ServiceName}Handler for the first entity.
        var singleMapping = $"app.MapGrpcService<{entities.First().ServiceName}Handler>();";
        return baseContent.Replace(singleMapping, mappingsStr);
    }

