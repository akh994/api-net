    private string GenerateGrpcHandler(Models.EntityDefinition entity, string projectName)
    {
        var methods = new List<string>();
        var entityNameLower = entity.Name.ToLower();

        foreach (var method in entity.Methods)
        {
            var methodName = method.Name;
            var inputType = GetFullyQualifiedProtoType(method.ProtoRequestType ?? method.RequestType, projectName);
            var outputType = GetFullyQualifiedProtoType(method.ProtoResponseType ?? method.ResponseType, projectName);
            string? methodImpl = null;

            // gRPC handler uses mapper to convert between proto and entity
            if (methodName.Equals("Add", StringComparison.OrdinalIgnoreCase) ||
                methodName.Equals("Create", StringComparison.OrdinalIgnoreCase))
            {
                methodImpl = $@"    public override async Task<{outputType}> {methodName}({inputType} request, ServerCallContext context)
    {{
        var entity = request.ToDomain();
        await _service.CreateAsync(entity);
        return new {outputType.Split('.').Last()} {{ Message = ""{entity.Name} created successfully"" }};
    }}";
            }
            else if (methodName.Equals("GetAll", StringComparison.OrdinalIgnoreCase))
            {
                var responseMsg = entity.Messages.Find(m => m.Name == method.ResponseType);
                var listField = responseMsg?.Fields.Find(f => f.IsRepeated);
                var listFieldName = listField?.Name ?? "Items";

                methodImpl = $@"    public override async Task<{outputType}> {methodName}({inputType} request, ServerCallContext context)
    {{
        var entities = await _service.GetAllAsync();
        var response = new {outputType.Split('.').Last()}();
        response.{listFieldName}.AddRange(entities.Select(e => e.ToProto()));
        return response;
    }}";
            }
            else if (methodName.Equals("GetById", StringComparison.OrdinalIgnoreCase))
            {
                methodImpl = $@"    public override async Task<{outputType}> {methodName}({inputType} request, ServerCallContext context)
    {{
        var entity = await _service.GetByIdAsync(request.Id);
        if (entity == null)
        {{
            throw new global::{projectName}.Common.Errors.NotFoundException($""{entity.Name} with ID {{request.Id}} not found"");
        }}
        return entity.ToProto();
    }}";
            }
            else if (methodName.Equals("Update", StringComparison.OrdinalIgnoreCase))
            {
                methodImpl = $@"    public override async Task<{outputType}> {methodName}({inputType} request, ServerCallContext context)
    {{
        var entity = request.ToDomain();
        await _service.UpdateAsync(request.Id, entity);
        return new {outputType.Split('.').Last()} {{ Message = ""{entity.Name} updated successfully"" }};
    }}";
            }
            else if (methodName.Equals("Delete", StringComparison.OrdinalIgnoreCase))
            {
                methodImpl = $@"    public override async Task<{outputType}> {methodName}({inputType} request, ServerCallContext context)
    {{
        await _service.DeleteAsync(request.Id);
        return new {outputType.Split('.').Last()} {{ Message = ""{entity.Name} deleted successfully"" }};
    }}";
            }
            else if (methodName.Equals("Search", StringComparison.OrdinalIgnoreCase))
            {
                var responseMsg = entity.Messages.Find(m => m.Name == method.ResponseType || m.Name == "Res" + entity.Name + "All");
                var listField = responseMsg?.Fields.Find(f => f.IsRepeated);
                var listFieldName = listField?.Name ?? "Items";

                methodImpl = $@"    public override async Task<{outputType}> {methodName}({inputType} request, ServerCallContext context)
    {{
        var entities = await _service.SearchAsync(request.Query);
        var response = new {outputType.Split('.').Last()}();
        response.{listFieldName}.AddRange(entities.Select(e => e.ToProto()));
        return response;
    }}";
            }
            else if (methodName.Equals("GetAllPaginated", StringComparison.OrdinalIgnoreCase))
            {
                var responseMsg = entity.Messages.Find(m => m.Name == method.ResponseType);
                var paginationField = responseMsg?.Fields.Find(f => f.Name.Equals("Pagination", StringComparison.OrdinalIgnoreCase));
                var paginationType = paginationField?.Type ?? $"{entity.Name}PaginationMeta";
                var listField = responseMsg?.Fields.Find(f => f.IsRepeated);
                var listFieldName = listField?.Name ?? "Items";

                methodImpl = $@"    public override async Task<{outputType}> {methodName}({inputType} request, ServerCallContext context)
    {{
        var page = request.Page > 0 ? request.Page : 1;
        var pageSize = request.PageSize > 0 ? request.PageSize : 10;

        var (items, totalItems) = await _service.GetAllPaginatedAsync(page, pageSize);

        var response = new {outputType}
        {{
            Pagination = new {paginationType}
            {{
                Page = page,
                PageSize = pageSize,
                TotalItems = totalItems,
                TotalPages = pageSize > 0 ? (int)Math.Ceiling((double)totalItems / pageSize) : 0
            }}
        }};
        response.{listFieldName}.AddRange(items.Select(e => e.ToProto()));

        return response;
    }}";
            }
            else
            {
                // Non-CRUD methods
                var inputDomainType = GetDomainType(method.RequestType);
                var outputDomainType = GetDomainType(method.ResponseType);

                string callParams = inputDomainType == "void" ? "" : "request.ToDomain()";
                string returnMapping = outputDomainType == "void" ? "" : ".ToProto()";

                methodImpl = $@"    public override async Task<{outputType}> {methodName}({inputType} request, ServerCallContext context)
    {{
        {(outputDomainType == "void" ? "" : "var result = ")}await _service.{methodName}Async({callParams});
        return {(outputDomainType == "void" ? $"new {outputType}()" : "result" + returnMapping)};
    }}";
            }
            // Non-CRUD methods are skipped

            if (methodImpl != null)
            {
                methods.Add(methodImpl);
            }
        }

        return $@"using Grpc.Core;
using {projectName}.Application.Interfaces;
using {projectName}.Mappers;
using {projectName}.Protos;

namespace {projectName}.Services;

public class {entity.ServiceName}Handler : {entity.ServiceName}.{entity.ServiceName}Base
{{
    private readonly I{entity.Name}Service _service;
    private readonly ILogger<{entity.ServiceName}Handler> _logger;

    public {entity.ServiceName}Handler(I{entity.Name}Service service, ILogger<{entity.ServiceName}Handler> logger)
    {{
        _service = service;
        _logger = logger;
    }}

{string.Join("\n\n", methods)}
}}";
    }

    private bool IsProtoScalar(string fieldType)
    {
        return new[] {
            "string", "int", "long", "double", "bool", "float", "byte[]",
            "string?", "int?", "long?", "double?", "bool?", "float?",
            "uint", "ulong", "uint?", "ulong?", "DateTime", "DateTime?",
            "decimal", "decimal?"
        }.Contains(fieldType);
    }

    private bool IsEnumType(string fieldType, Models.EntityDefinition entity)
    {
        return entity.Enums.Any(e => e.Name == fieldType);
    }

    private string GenerateMapper(Models.EntityDefinition entity, string projectName)
    {
        var mappers = new List<string>();

        foreach (var message in entity.Messages)
        {
            var toProtoMappings = new List<string>();
            var toDomainMappings = new List<string>();

            var toProtoPostMappings = new List<string>();

            foreach (var field in message.Fields)
            {
                var fieldName = field.Name;
                var fieldType = field.Type;

                if (field.IsRepeated)
                {
                    // Handle repeated fields
                    var isPrimitive = IsProtoScalar(fieldType);
                    if (isPrimitive)
                    {
                        toProtoPostMappings.Add($"        proto.{fieldName}.AddRange(entity.{fieldName});");
                        toDomainMappings.Add($"            {fieldName} = proto.{fieldName}.ToList()");
                    }
                    else
                    {
                        toProtoPostMappings.Add($"        proto.{fieldName}.AddRange(entity.{fieldName}.Select(x => x.ToProto()));");
                        toDomainMappings.Add($"            {fieldName} = proto.{fieldName}.Select(x => x.ToDomain()).ToList()");
                    }
                    continue;
                }

                // ToProto mapping (entity -> proto)
                // ToProto mapping (entity -> proto)
                if (fieldType == "string?")
                {
                    toProtoMappings.Add($"            {fieldName} = entity.{fieldName} ?? string.Empty");
                }
                else if (fieldType == "string")
                {
                    toProtoMappings.Add($"            {fieldName} = entity.{fieldName}");
                }
                else if (fieldType == "int?")
                {
                    toProtoMappings.Add($"            {fieldName} = entity.{fieldName}");
                }
                else if (fieldType == "int")
                {
                    toProtoMappings.Add($"            {fieldName} = entity.{fieldName}");
                }
                else if (fieldType == "long?")
                {
                    toProtoMappings.Add($"            {fieldName} = entity.{fieldName}");
                }
                else if (fieldType == "long")
                {
                    toProtoMappings.Add($"            {fieldName} = entity.{fieldName}");
                }
                else if (fieldType == "double?")
                {
                    toProtoMappings.Add($"            {fieldName} = entity.{fieldName}");
                }
                else if (fieldType == "double")
                {
                    toProtoMappings.Add($"            {fieldName} = entity.{fieldName}");
                }
                else if (fieldType == "decimal?")
                {
                    toProtoMappings.Add($"            {fieldName} = entity.{fieldName}.HasValue ? (double)entity.{fieldName}.Value : null");
                }
                else if (fieldType == "decimal")
                {
                    toProtoMappings.Add($"            {fieldName} = (double)entity.{fieldName}");
                }
                else if (fieldType == "bool?")
                {
                    toProtoMappings.Add($"            {fieldName} = entity.{fieldName}");
                }
                else if (fieldType == "bool")
                {
                    toProtoMappings.Add($"            {fieldName} = entity.{fieldName}");
                }
                else if (fieldType == "uint" || fieldType == "uint?")
                {
                    toProtoMappings.Add($"            {fieldName} = entity.{fieldName}");
                }
                else if (fieldType == "ulong" || fieldType == "ulong?")
                {
                    toProtoMappings.Add($"            {fieldName} = entity.{fieldName}");
                }
                else if (fieldType == "DateTime" || fieldType == "DateTime?")
                {
                    if (fieldType == "DateTime?")
                    {
                        toProtoMappings.Add($"            {fieldName} = entity.{fieldName}.HasValue ? Timestamp.FromDateTime(entity.{fieldName}.Value.ToUniversalTime()) : null");
                    }
                    else
                    {
                        toProtoMappings.Add($"            {fieldName} = Timestamp.FromDateTime(entity.{fieldName}.ToUniversalTime())");
                    }
                }
                else if (IsEnumType(fieldType, entity))
                {
                    // Enum types need explicit cast due to different namespace
                    var enumDef = entity.Enums.First(e => e.Name == fieldType);
                    var protoEnumType = enumDef.ProtoName ?? fieldType;
                    toProtoMappings.Add($"            {fieldName} = (Protos.{protoEnumType})entity.{fieldName}");
                }
                else
                {
                    // Nested message
                    toProtoMappings.Add($"            {fieldName} = entity.{fieldName}?.ToProto()");
                }

                // ToDomain mapping (proto -> entity)
                if (fieldType == "DateTime" || fieldType == "DateTime?")
                {
                    toDomainMappings.Add($"            {fieldName} = proto.{fieldName}?.ToDateTime() ?? DateTime.MinValue"); // Handle nullable timestamp
                }
                else if (fieldType == "decimal?")
                {
                    toDomainMappings.Add($"            {fieldName} = proto.{fieldName}.HasValue ? (decimal)proto.{fieldName}.Value : null");
                }
                else if (fieldType == "decimal")
                {
                    toDomainMappings.Add($"            {fieldName} = (decimal)proto.{fieldName}");
                }
                else if (IsProtoScalar(fieldType))
                {
                    // Primitives and nullable primitives match directly now (optional int32 -> int?)
                    toDomainMappings.Add($"            {fieldName} = proto.{fieldName}");
                }
                else if (IsEnumType(fieldType, entity))
                {
                    // Enum types need explicit cast
                    toDomainMappings.Add($"            {fieldName} = (Domain.Entities.{fieldType})proto.{fieldName}");
                }
                else
                {
                    // Nested message
                    toDomainMappings.Add($"            {fieldName} = proto.{fieldName}?.ToDomain()!");
                }
            }

            var protoClassName = message.ProtoName ?? message.Name;
            mappers.Add($@"    public static Protos.{protoClassName} ToProto(this Domain.Entities.{message.Name} entity)
    {{
        var proto = new Protos.{protoClassName}
        {{
{string.Join(",\n", toProtoMappings)}
        }};
{string.Join("\n", toProtoPostMappings)}
        return proto;
    }}

    public static Domain.Entities.{message.Name} ToDomain(this Protos.{protoClassName} proto)
    {{
        return new Domain.Entities.{message.Name}
        {{
{string.Join(",\n", toDomainMappings)}
        }};
    }}");
        }

        return $@"using Google.Protobuf.WellKnownTypes;
using {projectName}.Domain.Entities;
using {projectName}.Protos;

namespace {projectName}.Mappers;

public static class {entity.Name}Mapper
{{
{string.Join("\n\n", mappers)}
}}";
    }

