    private string GenerateRepositoryInterface(Models.EntityDefinition entity, string projectName)
    {
        var idType = GetIdType(entity);
        var methods = new List<string>();
        foreach (var method in entity.Methods)
        {
            var methodName = method.Name;
            string? methodSignature = null;

            // Repository only contains CRUD methods with entity types
            if (methodName.Equals("Add", StringComparison.OrdinalIgnoreCase) ||
                methodName.Equals("Create", StringComparison.OrdinalIgnoreCase))
            {
                methodSignature = $"    Task CreateAsync({entity.Name} {entity.Name.ToLower()});";
            }
            else if (methodName.Equals("GetAll", StringComparison.OrdinalIgnoreCase))
            {
                methodSignature = $"    Task<IEnumerable<{entity.Name}>> GetAllAsync();";
            }
            else if (methodName.Equals("GetById", StringComparison.OrdinalIgnoreCase))
            {
                methodSignature = $"    Task<{entity.Name}?> GetByIdAsync({idType} id);";
            }
            else if (methodName.Equals("Update", StringComparison.OrdinalIgnoreCase))
            {
                methodSignature = $"    Task UpdateAsync({entity.Name} {entity.Name.ToLower()});";
            }
            else if (methodName.Equals("Delete", StringComparison.OrdinalIgnoreCase))
            {
                methodSignature = $"    Task DeleteAsync({idType} id);";
            }
            else if (methodName.Equals("Search", StringComparison.OrdinalIgnoreCase))
            {
                methodSignature = $"    Task<IEnumerable<{entity.Name}>> SearchAsync(string query);";
            }
            else if (methodName.Equals("GetAllPaginated", StringComparison.OrdinalIgnoreCase))
            {
                methodSignature = $"    Task<(IEnumerable<{entity.Name}> Items, int TotalItems)> GetAllPaginatedAsync(int page, int pageSize);";
            }
            else
            {
                // Non-CRUD methods
                var inputDomainType = GetDomainType(method.RequestType);
                var outputDomainType = GetDomainType(method.ResponseType);

                string inputParam = inputDomainType == "void" ? "" : $"{inputDomainType} request";
                string returnType = outputDomainType == "void" ? "Task" : $"Task<{outputDomainType}>";

                methodSignature = $"    {returnType} {methodName}Async({inputParam});";
            }
            // Non-CRUD methods are included in repository for non-CRUD projects

            if (methodSignature != null)
            {
                methods.Add(methodSignature);
            }
        }

        return $@"using {projectName}.Domain.Entities;

namespace {projectName}.Application.Interfaces;

public interface I{entity.Name}Repository
{{
{string.Join("\n", methods)}
}}";
    }

