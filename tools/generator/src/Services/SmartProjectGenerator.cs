    }

    private async Task GenerateDomainAsync(Models.EntityDefinition entity, string projectName, string targetDir, bool dryRun)
    {
        var domainDir = Path.Combine(targetDir, "src", $"{projectName}.Domain");
        await CopyFileWithRenameAsync(
            Path.Combine(_skeletonPath, "src", "SkeletonApi.Domain", "SkeletonApi.Domain.csproj"),
            Path.Combine(domainDir, $"{projectName}.Domain.csproj"),
            projectName, dryRun);

        foreach (var message in entity.Messages)
        {
            var isMainEntity = message.Name == entity.Name;
            var content = GenerateDomainClass(message, projectName, isMainEntity);
            await WriteFileAsync(Path.Combine(domainDir, "Entities", $"{message.Name}.cs"), content, dryRun);
        }

        foreach (var @enum in entity.Enums)
        {
            var content = GenerateDomainEnum(@enum, projectName);
            await WriteFileAsync(Path.Combine(domainDir, "Entities", $"{@enum.Name}.cs"), content, dryRun);
        }
    }

    private string GenerateDomainClass(Models.MessageDefinition message, string projectName, bool isMainEntity)
    {
        var fields = new List<string>();

        if (isMainEntity && !message.Fields.Any(f => f.Name.Equals("Id", StringComparison.OrdinalIgnoreCase)))
        {
            fields.Add("    public string Id { get; set; } = string.Empty;");
        }

        foreach (var field in message.Fields)
        {
            var type = field.Type;
            var name = field.Name;

            if (field.IsRepeated)
            {
                type = $"List<{type}>";
            }

            var defaultValue = type switch
            {
                "string" => " = string.Empty;",
                "int" => " = 0;",
                "uint" => " = 0;",
                "long" => " = 0;",
                "ulong" => " = 0;",
                "bool" => " = false;",
                "double" => " = 0.0;",
                "float" => " = 0.0f;",
                "DateTime" => " = DateTime.UtcNow;",
                "byte[]" => " = Array.Empty<byte>();",
                _ => type.StartsWith("List<") ? " = new();" : (type.EndsWith("?") ? "" : " = default!;")
            };

            fields.Add($"    public {type} {name} {{ get; set; }}{defaultValue}");
        }

        if (isMainEntity)
        {
            if (!message.Fields.Any(f => f.Name.Equals("CreatedAt", StringComparison.OrdinalIgnoreCase)))
                fields.Add("    public DateTime CreatedAt { get; set; } = DateTime.UtcNow;");

            if (!message.Fields.Any(f => f.Name.Equals("UpdatedAt", StringComparison.OrdinalIgnoreCase)))
                fields.Add("    public DateTime UpdatedAt { get; set; } = DateTime.UtcNow;");
        }

        return $@"namespace {projectName}.Domain.Entities;

public class {message.Name}
{{
{string.Join("\n", fields)}
}}";
    }

    private string GenerateDomainEnum(Models.EnumDefinition @enum, string projectName)
    {
        var values = @enum.Values.Select((v, i) => $"    {v} = {i},").ToList();
        return $@"namespace {projectName}.Domain.Entities;

public enum {@enum.Name}
{{
{string.Join("\n", values)}
}}";
    }

