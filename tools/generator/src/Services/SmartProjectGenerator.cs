    }

    private string GetFullyQualifiedProtoType(string type, string projectName)
    {
        if (type == "Empty" || type == "google.protobuf.Empty") return "Google.Protobuf.WellKnownTypes.Empty";

        if (type.StartsWith("google.protobuf."))
        {
            return type switch
            {
                "google.protobuf.Timestamp" => "Google.Protobuf.WellKnownTypes.Timestamp",
                "google.protobuf.StringValue" => "Google.Protobuf.WellKnownTypes.StringValue",
                "google.protobuf.Int32Value" => "Google.Protobuf.WellKnownTypes.Int32Value",
                "google.protobuf.Int64Value" => "Google.Protobuf.WellKnownTypes.Int64Value",
                "google.protobuf.BoolValue" => "Google.Protobuf.WellKnownTypes.BoolValue",
                "google.protobuf.DoubleValue" => "Google.Protobuf.WellKnownTypes.DoubleValue",
                "google.protobuf.FloatValue" => "Google.Protobuf.WellKnownTypes.FloatValue",
                _ => type.Replace("google.protobuf.", "Google.Protobuf.WellKnownTypes.")
            };
        }
        return $"{projectName}.Protos.{type}";
    }

    private string GetDomainType(string protoType)
    {
        if (protoType == "Empty" || protoType == "google.protobuf.Empty")
        {
            return "void";
        }

        if (protoType.StartsWith("google.protobuf."))
        {
            return protoType switch
            {
                "google.protobuf.Timestamp" => "DateTime",
                "google.protobuf.StringValue" => "string?",
                "google.protobuf.Int32Value" => "int?",
                "google.protobuf.Int64Value" => "long?",
                "google.protobuf.BoolValue" => "bool?",
                "google.protobuf.DoubleValue" => "double?",
                "google.protobuf.FloatValue" => "float?",
                _ => "string"
            };
        }
        return protoType;
    }

    private string GetIdType(Models.EntityDefinition entity)
    {
        // Find ID field in entity fields
        var idField = entity.Fields.Find(f => f.Name.Equals("Id", StringComparison.OrdinalIgnoreCase));
        if (idField != null)
        {
            return idField.Type;
        }

        // Default to string for backward compatibility
        return "string";
    }

    private string GenerateIdAssignment(string idType, string entityNameLower)
    {
        return idType switch
        {
            "int" or "long" => $"// {entityNameLower}.Id will be auto-generated by database",
            "Guid" => $"{entityNameLower}.Id = Guid.NewGuid();",
            _ => $"{entityNameLower}.Id = Guid.NewGuid().ToString();"
        };
    }

    public async Task GenerateAsync(
        Models.EntityDefinition entity,
        string projectName,
        string outputDir,
        bool dryRun,
        bool includeMq = false,
        string dbProvider = "mysql",
        bool onlyDomainAndContracts = false,
        bool update = false,
        bool onlyDomainAndMapper = false)
    {
        var targetDir = string.IsNullOrEmpty(outputDir) ? projectName : outputDir;
        targetDir = Path.GetFullPath(targetDir);

        if (Directory.Exists(targetDir) && !dryRun && !onlyDomainAndContracts && !onlyDomainAndMapper && !update)
        {
            Directory.Delete(targetDir, true);
        }

        AnsiConsole.MarkupLine($"[blue]Generating project: {projectName}[/]");
        AnsiConsole.MarkupLine($"[blue]Entity: {entity.Name} with {entity.Methods.Count} methods[/]");

        if (onlyDomainAndContracts)
        {
            await GenerateDomainAsync(entity, projectName, targetDir, dryRun);
            await GenerateContractsAsync(entity, projectName, targetDir, dryRun);
            AnsiConsole.MarkupLine($"\n[green]Domain and Contracts updated successfully![/]");
            return;
        }

        if (onlyDomainAndMapper)
        {
            await GenerateDomainAsync(entity, projectName, targetDir, dryRun);
            var apiDir = Path.Combine(targetDir, "src", projectName);
            var mapperContent = GenerateMapper(entity, projectName);
            await WriteFileAsync(Path.Combine(apiDir, "Mappers", $"{entity.Name}Mapper.cs"), mapperContent, dryRun, projectName);
            AnsiConsole.MarkupLine($"\n[green]Domain and Mapper updated successfully![/]");
            return;
        }

        if (!update && _pkgType == "local")
        {
            await CopyDevOpsFilesAsync(targetDir, projectName, entity, dryRun);
            await CopyDirectoryWithRenameAsync(
                Path.Combine(_skeletonPath, "src", "SkeletonApi.Common"),
                Path.Combine(targetDir, "src", $"{projectName}.Common"),
                projectName, dryRun);
            await GenerateDirectoryBuildPropsAsync(targetDir, dryRun);
        }
        else if (!update && _pkgType == "remote")
        {
            await CopyDevOpsFilesAsync(targetDir, projectName, entity, dryRun);
            await GenerateNuGetConfigAsync(targetDir, dryRun);
            await GenerateDirectoryBuildPropsAsync(targetDir, dryRun);
        }

        await GenerateDomainAsync(entity, projectName, targetDir, dryRun);

        // Handle Contracts based on update mode
        if (!update)
        {
            await GenerateContractsAsync(entity, projectName, targetDir, dryRun);
        }
        else
        {
            // In update mode, ensure proto file is added/updated in the Contracts project
            await AppendProtoFilesToContractsAsync(new List<Models.EntityDefinition> { entity }, projectName, targetDir, dryRun);
        }

        await GenerateApplicationAsync(entity, projectName, targetDir, dryRun, includeMq);
        await GenerateInfrastructureAsync(entity, projectName, targetDir, dryRun, includeMq, dbProvider);

        // Generate API (including gRPC Handler and Mapper)
        // In update mode, we skip common files (Program.cs, DI) because we inject them instead
        await GenerateApiAsync(entity, projectName, targetDir, dryRun, includeMq, dbProvider, !update);

        if (update)
        {
            await InjectRegistrationsAsync(entity, projectName, targetDir, dryRun, includeMq);
        }

        // Generate migrations
        await GenerateMigrationsAsync(entity, targetDir, dryRun);

        await GenerateTestsAsync(targetDir, projectName, dryRun);
        await GenerateEntityTestsAsync(entity, projectName, targetDir, dryRun);
        var readme = GenerateReadme(entity, projectName, includeMq);
        await WriteFileAsync(Path.Combine(targetDir, "README.md"), readme, dryRun, projectName, entity.Name);

        if (!dryRun && !update)
        {
            await CreateSolutionAsync(targetDir, projectName);
        }

        // Always update project references/versions at the end
        await UpdateProjectReferencesAsync(targetDir, projectName, dryRun);


        if (!dryRun)
        {
            // Format generated code
            AnsiConsole.MarkupLine("[blue]Formatting generated code...[/]");
            await RunCommandAsync("dotnet", "format --no-restore", targetDir);

            AnsiConsole.MarkupLine($"\n[green]Project generated successfully![/]");
        }
    }

    /// <summary>
    /// Generate project with multiple entities from SQL schema
    /// </summary>
    public async Task GenerateMultipleEntitiesAsync(
        List<Models.EntityDefinition> entities,
        string projectName,
        string outputDir,
        bool dryRun,
        bool includeMq = false,
        string dbProvider = "mysql",
        bool update = false)
    {
        var targetDir = string.IsNullOrEmpty(outputDir) ? projectName : outputDir;
        targetDir = Path.GetFullPath(targetDir);

        // Only delete directory if NOT in update mode
        if (Directory.Exists(targetDir) && !dryRun && !update)
        {
            Directory.Delete(targetDir, true);
        }

        AnsiConsole.MarkupLine($"[blue]Generating project: {projectName}[/]");
        AnsiConsole.MarkupLine($"[blue]Entities: {entities.Count} tables[/]");



        // Only copy DevOps files and Common if NOT in update mode
        if (!update && _pkgType == "local")
        {
            var firstEntity = entities[0];
            await CopyDevOpsFilesAsync(targetDir, projectName, firstEntity, dryRun);
            await CopyDirectoryWithRenameAsync(
                Path.Combine(_skeletonPath, "src", "SkeletonApi.Common"),
                Path.Combine(targetDir, "src", $"{projectName}.Common"),
                projectName, dryRun);
            await GenerateDirectoryBuildPropsAsync(targetDir, dryRun);
        }
        else if (!update && _pkgType == "remote")
        {
            var firstEntity = entities[0];
            await CopyDevOpsFilesAsync(targetDir, projectName, firstEntity, dryRun);
            await GenerateNuGetConfigAsync(targetDir, dryRun);
            await GenerateDirectoryBuildPropsAsync(targetDir, dryRun);
        }

        // Generate Contracts project once with all proto files
        if (!update)
        {
            await GenerateMultiEntityContractsAsync(entities, projectName, targetDir, dryRun);
        }
        else
        {
            // In update mode, append new proto files to existing Contracts project
            await AppendProtoFilesToContractsAsync(entities, projectName, targetDir, dryRun);
        }

        // Generate all entities
        for (int i = 0; i < entities.Count; i++)
        {
            var entity = entities[i];

            AnsiConsole.MarkupLine($"[blue]  Generating {entity.Name}...[/]");

            await GenerateDomainAsync(entity, projectName, targetDir, dryRun);
            // Skip GenerateContractsAsync here - already done above
            await GenerateApplicationAsync(entity, projectName, targetDir, dryRun, includeMq);
            await GenerateInfrastructureAsync(entity, projectName, targetDir, dryRun, includeMq, dbProvider);

            // In update mode, skip common files for each entity
            await GenerateApiAsync(entity, projectName, targetDir, dryRun, includeMq, dbProvider, !update);

            if (update)
            {
                await InjectRegistrationsAsync(entity, projectName, targetDir, dryRun, includeMq);
            }

            // Generate migrations for each entity with an offset to ensure unique timestamps
            await GenerateMigrationsAsync(entity, targetDir, dryRun, i);
        }

        // Generate common files only for fresh project
        if (!update)
        {
            await GenerateCommonFilesAsync(entities, projectName, targetDir, dryRun, includeMq);
        }

        await GenerateTestsAsync(targetDir, projectName, dryRun);
        foreach (var entity in entities)
        {
            await GenerateEntityTestsAsync(entity, projectName, targetDir, dryRun);
        }

        // Generate README with all entities
        var readme = GenerateMultiEntityReadme(entities, projectName, includeMq);
        await WriteFileAsync(Path.Combine(targetDir, "README.md"), readme, dryRun, projectName);

        if (!dryRun)
        {
            await CreateSolutionAsync(targetDir, projectName);
        }

        // Always update project references/versions at the end
        await UpdateProjectReferencesAsync(targetDir, projectName, dryRun);

        if (!dryRun)
        {
            // Format generated code
            AnsiConsole.MarkupLine("[blue]Formatting generated code...[/]");
            await RunCommandAsync("dotnet", "format", targetDir);

            AnsiConsole.MarkupLine($"\n[green]Project generated successfully with {entities.Count} entities![/]");
        }
    }

    private async Task InjectRegistrationsAsync(Models.EntityDefinition entity, string projectName, string targetDir, bool dryRun, bool includeMq)
    {
        var apiDir = Path.Combine(targetDir, "src", projectName);
        var programPath = Path.Combine(apiDir, "Program.cs");
        var diPath = Path.Combine(apiDir, "Extensions", "DependencyInjection.cs");

        await InjectGrpcServiceMappingAsync(programPath, entity, projectName, dryRun);
        await InjectDependencyInjectionAsync(diPath, entity, projectName, dryRun);
    }

    private async Task InjectGrpcServiceMappingAsync(string filePath, Models.EntityDefinition entity, string projectName, bool dryRun)
    {
        if (!File.Exists(filePath)) return;

        var content = await File.ReadAllTextAsync(filePath);
        var registration = $"app.MapGrpcService<{entity.ServiceName}Handler>();";

        if (!content.Contains(registration))
        {
            // Find the last MapGrpcService call
            var matches = Regex.Matches(content, @"app\.MapGrpcService<[^>]+>\(\);");
            if (matches.Count > 0)
            {
                var lastMatch = matches[matches.Count - 1];
                var insertPos = lastMatch.Index + lastMatch.Length;
                content = content.Insert(insertPos, $"\n{registration}");
                await WriteFileAsync(filePath, content, dryRun, projectName);
            }
        }
    }

    private async Task InjectDependencyInjectionAsync(string filePath, Models.EntityDefinition entity, string projectName, bool dryRun)
    {
        if (!File.Exists(filePath)) return;

        // 1. Inject Application Service
        var appRegistration = $"services.AddScoped<I{entity.Name}Service, global::{projectName}.Application.Services.{entity.Name}Service>();";
        _codeInjector.InjectDependency(filePath, appRegistration, "AddApplication");

        // 2. Inject Infrastructure Repository
        var infraRegistration = $"services.AddScoped<I{entity.Name}Repository, global::{projectName}.Infrastructure.Repositories.{entity.Name}Repository>();";
        _codeInjector.InjectDependency(filePath, infraRegistration, "AddInfrastructure");
    }

    private async Task CopyDevOpsFilesAsync(string targetDir, string projectName, Models.EntityDefinition entity, bool dryRun)
    {
        var directories = new[] { "config", "credentials", "deployments" };
        foreach (var dir in directories)
        {
            var srcDir = Path.Combine(_skeletonPath, dir);
            if (Directory.Exists(srcDir))
            {
                await CopyDirectoryWithRenameAsync(srcDir, Path.Combine(targetDir, dir), projectName, dryRun, entity.Name);
            }
        }

        // Copy HealthCheck tool
        var healthCheckSrc = Path.Combine(_skeletonPath, "tools", "HealthCheck");
        if (Directory.Exists(healthCheckSrc))
        {
            await CopyDirectoryWithRenameAsync(healthCheckSrc, Path.Combine(targetDir, "tools", "HealthCheck"), projectName, dryRun, entity.Name);
        }

        await GenerateMigrationsAsync(entity, targetDir, dryRun);

        if (!dryRun)
        {
            Directory.CreateDirectory(Path.Combine(targetDir, "docs"));
        }

        // Copy steering docs
        var steeringSrc = Path.Combine(_skeletonPath, "docs", "steering");
        if (Directory.Exists(steeringSrc))
        {
            await CopyDirectoryWithRenameAsync(steeringSrc, Path.Combine(targetDir, "docs", "steering"), projectName, dryRun, entity.Name);
            await UpdateProductDocAsync(targetDir, projectName, dryRun);
        }

        var files = new[]
        {
            ".gitignore", ".dockerignore", "build.sh", "deploy.sh",
            "Dockerfile.rabbitmq", "Jenkinsfile", "Makefile",
            "setup-dependencies.sh", "sonar-project.properties", "README.md"
        };

        foreach (var file in files)
        {
            var srcFile = Path.Combine(_skeletonPath, file);
            if (File.Exists(srcFile))
            {
                await CopyFileWithRenameAsync(srcFile, Path.Combine(targetDir, file), projectName, dryRun, entity.Name);
            }
        }
    }

