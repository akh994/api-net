    private async Task CreateSolutionAsync(string targetDir, string projectName)
    {
        // Detect .NET SDK version
        var version = await GetDotNetSdkVersionAsync();

        // Use --format sln only for .NET 9+ to ensure .sln format instead of .slnx
        var formatFlag = version >= new Version(9, 0) ? " --format sln" : "";

        // Create solution
        await RunCommandAsync("dotnet", $"new sln -n \"{projectName}\"{formatFlag}", targetDir);

        // Add projects
        var projects = new[]
        {
            Path.Combine("src", projectName, $"{projectName}.csproj"),
            Path.Combine("src", $"{projectName}.Application", $"{projectName}.Application.csproj"),
            Path.Combine("src", $"{projectName}.Domain", $"{projectName}.Domain.csproj"),
            Path.Combine("src", $"{projectName}.Infrastructure", $"{projectName}.Infrastructure.csproj"),
            Path.Combine("src", $"{projectName}.Common", $"{projectName}.Common.csproj"),
            Path.Combine("src", $"{projectName}.Contracts", $"{projectName}.Contracts.csproj"),
            Path.Combine("tests", $"{projectName}.Tests", $"{projectName}.Tests.csproj"),
            Path.Combine("tools", "HealthCheck", "HealthCheck.csproj")
        };

        foreach (var project in projects)
        {
            // Check if project file exists before adding
            if (File.Exists(Path.Combine(targetDir, project)))
            {
                await RunCommandAsync("dotnet", $"sln add \"{project}\"", targetDir);
            }
        }
    }

    private async Task<Version> GetDotNetSdkVersionAsync()
    {
        var psi = new System.Diagnostics.ProcessStartInfo
        {
            FileName = "dotnet",
            Arguments = "--version",
            RedirectStandardOutput = true,
            RedirectStandardError = true,
            UseShellExecute = false,
            CreateNoWindow = true
        };

        using var process = System.Diagnostics.Process.Start(psi);
        if (process != null)
        {
            var output = (await process.StandardOutput.ReadToEndAsync()).Trim();
            await process.WaitForExitAsync();

            // Parse version string (e.g., "9.0.203" or "10.0.100-preview" -> Version)
            var versionPart = output.Split('-')[0];
            if (Version.TryParse(versionPart, out var version))
            {
                return version;
            }
        }

        // Default to 8.0 if detection fails (safe fallback - no --format flag)
        return new Version(8, 0);
    }

    private async Task CopyDirectoryWithRenameAsync(string srcDir, string targetDir, string projectName, bool dryRun, string? entityName = null)
    {
        if (!Directory.Exists(srcDir)) return;
        if (!dryRun) Directory.CreateDirectory(targetDir);

        foreach (var dir in Directory.GetDirectories(srcDir))
        {
            var dirName = Path.GetFileName(dir);
            if (dirName == "obj" || dirName == "bin" || dirName == ".git") continue;
            await CopyDirectoryWithRenameAsync(dir, Path.Combine(targetDir, RenameIdentifier(dirName, projectName, entityName)), projectName, dryRun, entityName);
        }

        foreach (var file in Directory.GetFiles(srcDir))
        {
            await CopyFileWithRenameAsync(file, Path.Combine(targetDir, RenameIdentifier(Path.GetFileName(file), projectName, entityName)), projectName, dryRun, entityName);
        }
    }

