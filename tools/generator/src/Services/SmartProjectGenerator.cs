    private async Task AppendProtoFilesToContractsAsync(
        List<Models.EntityDefinition> entities,
        string projectName,
        string targetDir,
        bool dryRun)
    {
        var contractsDir = Path.Combine(targetDir, "src", $"{projectName}.Contracts");
        var protosDir = Path.Combine(contractsDir, "Protos");

        if (!dryRun && !Directory.Exists(protosDir))
        {
            Directory.CreateDirectory(protosDir);
        }

        var allProtos = new HashSet<string>(StringComparer.OrdinalIgnoreCase);

        // Write new proto files and handle imports
        foreach (var entity in entities)
        {
            var protoContent = entity.ProtoContent;
            if (string.IsNullOrEmpty(protoContent)) continue;

            protoContent = EnsureHttpAnnotations(protoContent, entity.Name);

            if (protoContent.Contains("option csharp_namespace"))
            {
                protoContent = Regex.Replace(protoContent,
                    @"option csharp_namespace = ""[^""]+"";",
                    $@"option csharp_namespace = ""{projectName}.Protos"";");
            }
            else
            {
                var packageMatch = Regex.Match(protoContent, @"package\s+[^;]+;");
                if (packageMatch.Success)
                {
                    protoContent = protoContent.Insert(packageMatch.Index + packageMatch.Length,
                        $"\n\noption csharp_namespace = \"{projectName}.Protos\";");
                }
            }

            var mainProtoName = $"{entity.Name.ToLower()}.proto";
            var protoPath = Path.Combine(protosDir, mainProtoName);
            allProtos.Add(mainProtoName);

            await WriteFileAsync(protoPath, protoContent, dryRun, projectName);

            // Handle imports
            if (!string.IsNullOrEmpty(entity.SourcePath))
            {
                var sourceDir = Path.GetDirectoryName(entity.SourcePath);
                // More permissive regex for imports
                var importMatches = Regex.Matches(protoContent, @"import\s+(?:public\s+|weak\s+)?[""']([^""']+)[""']\s*;");

                AnsiConsole.MarkupLine($"[blue]Found {importMatches.Count} imports in {entity.Name} (SourcePath: {entity.SourcePath})[/]");

                foreach (Match importMatch in importMatches)
                {
                    var importFile = importMatch.Groups[1].Value;
                    AnsiConsole.MarkupLine($"[blue]Processing import: {importFile}[/]");
                    if (importFile.StartsWith("google/")) continue;

                    var searchFile = Path.GetFileName(importFile);
                    var sourceImportPath = FindFileRecursive(sourceDir ?? "", searchFile);

                    if (sourceImportPath != null)
                    {
                        var targetImportPath = Path.Combine(protosDir, searchFile);
                        var importContent = await File.ReadAllTextAsync(sourceImportPath);

                        // Update C# namespace in imported proto if exists
                        if (importContent.Contains("option csharp_namespace"))
                        {
                            importContent = Regex.Replace(importContent,
                                @"option csharp_namespace = ""[^""]+"";",
                                $@"option csharp_namespace = ""{projectName}.Protos"";");
                        }
                        else
                        {
                            var packageMatch = Regex.Match(importContent, @"package\s+[^;]+;");
                            if (packageMatch.Success)
                            {
                                importContent = importContent.Insert(packageMatch.Index + packageMatch.Length,
                                    $"\n\noption csharp_namespace = \"{projectName}.Protos\";");
                            }
                        }

                        await WriteFileAsync(targetImportPath, importContent, dryRun, projectName);
                        allProtos.Add(searchFile);
                        AnsiConsole.MarkupLine($"[green]Copied imported proto: {searchFile} (found at {sourceImportPath})[/]");
                    }
                    else
                    {
                        AnsiConsole.MarkupLine($"[yellow]Warning: Imported proto file not found: {importFile}[/]");
                    }
                }
            }
        }

        // Append to existing csproj
        var csprojPath = Path.Combine(contractsDir, $"{projectName}.Contracts.csproj");
        var updated = false;
        var content = string.Empty;

        if (!dryRun && File.Exists(csprojPath))
        {
            content = await File.ReadAllTextAsync(csprojPath);

            // NEW APPROACH: Clean up all existing Protobuf references and their ItemGroups
            content = Regex.Replace(content, @"<ItemGroup>\s*(?:<Protobuf[^>]*/>\s*)+\s*</ItemGroup>", "", RegexOptions.Singleline);
            content = Regex.Replace(content, @"<Protobuf[^>]*/>", "");

            var protoIncludes = string.Join("\n    ", allProtos.Select(p =>
                $"<Protobuf Include=\"Protos\\{p}\" GrpcServices=\"Both\" ProtoRoot=\"Protos\" />"));

            var newItemGroup = $"\n  <ItemGroup>\n    {protoIncludes}\n  </ItemGroup>\n";

            // Add before </Project>
            content = content.Replace("</Project>", $"{newItemGroup}</Project>");
            updated = true;
        }

        if (updated && !string.IsNullOrEmpty(content))
        {
            await File.WriteAllTextAsync(csprojPath, content);
            AnsiConsole.MarkupLine($"[green]Updated {projectName}.Contracts.csproj with {allProtos.Count} proto references[/]");
        }
    }

    private string? FindFileRecursive(string dir, string fileName)
    {
        if (!Directory.Exists(dir)) return null;

        var files = Directory.GetFiles(dir, fileName, SearchOption.AllDirectories);
        if (files.Any()) return files[0];

        // Case-insensitive fallback
        var allFiles = Directory.GetFiles(dir, "*", SearchOption.AllDirectories);
        return Array.Find(allFiles, f => Path.GetFileName(f).Equals(fileName, StringComparison.OrdinalIgnoreCase));
    }

    private async Task GenerateApplicationAsync(Models.EntityDefinition entity, string projectName, string targetDir, bool dryRun, bool includeMq, bool generateCommonFiles = true)
    {
        var appDir = Path.Combine(targetDir, "src", $"{projectName}.Application");

        if (generateCommonFiles)
        {
            await CopyFileWithRenameAsync(
                Path.Combine(_skeletonPath, "src", "SkeletonApi.Application", "SkeletonApi.Application.csproj"),
                Path.Combine(appDir, $"{projectName}.Application.csproj"),
