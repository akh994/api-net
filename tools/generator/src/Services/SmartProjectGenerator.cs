    private async Task RunCommandAsync(string command, string args, string workingDir)
    {
        var psi = new System.Diagnostics.ProcessStartInfo
        {
            FileName = command,
            Arguments = args,
            WorkingDirectory = workingDir,
            RedirectStandardOutput = true,
            RedirectStandardError = true,
            UseShellExecute = false,
            CreateNoWindow = true
        };

        using var process = System.Diagnostics.Process.Start(psi);
        if (process != null)
        {
            await process.WaitForExitAsync();
            if (process.ExitCode != 0)
            {
                var error = await process.StandardError.ReadToEndAsync();
                AnsiConsole.MarkupLine($"[red]Error running {command} {Markup.Escape(args)}: {Markup.Escape(error)}[/]");
            }
        }
    }

    private string ReplacePlaceholders(string content, string projectName, string? entityName = null)
    {
        var kebabCase = ToKebabCase(projectName);
        var snakeCase = ToSnakeCase(projectName);

        var result = _codeInjector.ReplacePlaceholders(content, projectName);

        result = result
            .Replace("skeleton-api-net.sln", $"{projectName}.sln")
            .Replace("SkeletonApi", projectName)
            .Replace("skeleton-api-net", kebabCase)
            .Replace("skeleton-", $"{kebabCase}-")
            .Replace("skeleton", snakeCase)
            .Replace("COPY tools/generator/src/*.csproj ./tools/generator/src/", "");

        if (_pkgType.Equals("remote", StringComparison.OrdinalIgnoreCase))
        {
            // Handle case where code was generated with projectName.Common
            result = result.Replace($"{projectName}.Common", _commonPkgName);
        }

        if (!string.IsNullOrEmpty(entityName))
        {
            result = result.Replace("User", entityName);
            result = result.Replace("user", entityName.ToLower());
        }

        return result;
    }



    private string ToKebabCase(string text)
    {
        if (string.IsNullOrEmpty(text)) return text;
        return System.Text.RegularExpressions.Regex.Replace(text, "([a-z])([A-Z])", "$1-$2").ToLower();
    }

    private async Task UpdateAppSettingsDbProvider(string filePath, string dbProvider)
    {
        try
        {
            var content = await File.ReadAllTextAsync(filePath);
            var jsonNode = JsonNode.Parse(content);
            if (jsonNode?["Database"] is JsonNode databaseSection)
            {
                databaseSection["Provider"] = dbProvider.ToLower();

                var port = dbProvider.ToLower() switch
                {
                    "postgresql" => 5432,
